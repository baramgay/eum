-- ─────────────────────────────────────────────
-- 5. 성능 인덱스
-- ─────────────────────────────────────────────
CREATE INDEX IF NOT EXISTS idx_gyp_sigun       ON gold_youth_population(sigun);
CREATE INDEX IF NOT EXISTS idx_gsi_rank        ON gold_settlement_index(rank);
CREATE INDEX IF NOT EXISTS idx_gsi_score       ON gold_settlement_index(settlement_score DESC);
CREATE INDEX IF NOT EXISTS idx_onto_obj_type   ON onto_objects(obj_type);
CREATE INDEX IF NOT EXISTS idx_onto_link_src   ON onto_links(src);
CREATE INDEX IF NOT EXISTS idx_catalog_open    ON catalog(is_open, ai_ready);
CREATE INDEX IF NOT EXISTS idx_quality_passed  ON quality_results(passed);

-- ─────────────────────────────────────────────
-- 6. catalog 공개 읽기 RLS (gold 테이블도 허용)
-- ─────────────────────────────────────────────
ALTER TABLE gold_settlement_index ENABLE ROW LEVEL SECURITY;
DO $$ BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_policies WHERE tablename = 'gold_settlement_index' AND policyname = 'gsi_public_read'
  ) THEN
    CREATE POLICY "gsi_public_read" ON gold_settlement_index FOR SELECT USING (true);
  END IF;
END $$;
