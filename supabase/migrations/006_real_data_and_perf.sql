-- ─────────────────────────────────────────────
-- 2. 정착잠재지수 테이블 신설
-- ─────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS gold_settlement_index (
  sgg_cd                  TEXT PRIMARY KEY,
  sigun                   TEXT NOT NULL,
  gov_type                TEXT,               -- '시' | '군'
  youth_pop_2025          DOUBLE PRECISION,   -- 등록인구
  youth_living_2025       DOUBLE PRECISION,   -- 주간 생활인구 평균
  youth_net_migration     DOUBLE PRECISION,   -- 타지역 순유입 이동량 (2024-2025)
  youth_pop_change_rate   DOUBLE PRECISION,   -- 2018→2025 증감률 (%)
  income_monthly          DOUBLE PRECISION,   -- 월평균추산소득 (만원/인)
  credit_score_avg        DOUBLE PRECISION,   -- 평균신용점수 (점)
  loan_per_cap            DOUBLE PRECISION,   -- 1인당 대출잔액 (만원)
  card_3m_per_cap         DOUBLE PRECISION,   -- 1인당 3개월 카드이용액 (만원)
  living_index            DOUBLE PRECISION,   -- 청년생활인구지수 (생활/등록 비율 ×100)
  settlement_score        DOUBLE PRECISION,   -- 정착잠재지수 (표준화 종합)
  rank                    INT,
  updated_at              TEXT DEFAULT (to_char(now(), 'YYYY-MM-DD'))
);

